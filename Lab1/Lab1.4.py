# List initialization
numbers = [1, 2, 3, 4, 5]

# Accessing element
print("First element:", numbers[0])      #output 1
print("Last element:", numbers[-1])      #output 5

#slicing
print("sliced element", numbers[2:4])    #output [3, 4]

# Appending and extending
numbers.append(6)
print("after append:", numbers)          #output [1,2,3,4,5]

#removing element
numbers.remove
print("after removal:", numbers)